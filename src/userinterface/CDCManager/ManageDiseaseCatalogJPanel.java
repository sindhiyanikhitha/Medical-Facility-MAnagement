    private void removeDiseaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeDiseaseBtnActionPerformed
        // TODO add your handling code here:
         int selectedRow = diseaseCatalogTable.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }
        
        Disease disease = (Disease) diseaseCatalogTable.getValueAt(selectedRow, 1);
        diseaseList.removeDisease(disease);
        populateTable();
        
    }//GEN-LAST:event_removeDiseaseBtnActionPerformed

