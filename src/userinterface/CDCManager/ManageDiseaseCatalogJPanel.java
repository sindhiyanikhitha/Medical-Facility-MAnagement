    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) diseaseCatalogTable.getModel();
        
        model.setRowCount(0);
        
        int i = 0;
        for(Disease disease: diseaseList.getDiseaseList()){
            Object[] row = new Object[2];
            row[0] = ++i;
            row[1] = disease;
            model.addRow(row);
        }
    
    
    }

