    private void removeVaccineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeVaccineBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = vaccineCatalogTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        Disease disease = (Disease) vaccineCatalogTable.getValueAt(selectedRow, 1);
        diseaseList.removeDisease(disease);
        populateTable();

    }//GEN-LAST:event_removeVaccineBtnActionPerformed

