    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) vaccineCatalogTable.getModel();
        
        model.setRowCount(0);
        
        int i = 0;
        for(Vaccine vaccine: vaccineList.getVaccineList()){
            Object[] row = new Object[3];
            row[0] = ++i;
            row[1] = vaccine;
            row[2] = vaccine.getDiseaseUsedFor();
            model.addRow(row);
        }
    
    
    }

